package me.hsy.mycanvas.ui.main.pie;

public class PieBean {
    private int color;
    private float value;
    private String item;

    public int getColor() {
        return color;
    }
    public void setColor(int color) {
        this.color = color;
    }

    public float getValue() {
        return value;
    }
    public void setValue(float value) {
        this.value = value;
    }

    public String getItem() {
        return item;
    }
    public void setItem(String item) {
        this.item = item;
    }
}
